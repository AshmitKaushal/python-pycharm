'''Formatted strings, also known as f-strings, were introduced in Python 3.6 as a concise and convenient way to embed expressions inside string literals.

They provide a more readable and expressive way to create strings with dynamic content.

In an f-string, expressions inside curly braces {} are evaluated and their values are inserted into the string.

f-string: An f-string is a string prefixed with an 'f' or 'F'

Here's a brief overview:'''

